const producto = {
    nombre: "Monitor 20 pulgadas",
    precio: 300,
    disponible: true
}
console.log(Object.keys(producto)); // Object.keys() returns an array with the keys of the object
console.log(Object.values(producto));   // Object.values() returns an array with the values of the object
console.log(Object.entries(producto));  // Object.entries() returns an array with the keys and values of the object

