// undefined 
let num04_1;
console.log(num04_1); // undefined
console.log(typeof num04_1); // undefined

// null NOTA: null es un objeto
let num04_2 = null;
console.log(num04_2); // null
console.log(typeof num04_2); // object

// es igual porque no tiene valor
console.log(num04_1 == num04_2); // true

// no es estrictamente igual porque no son del mismo tipo
console.log(num04_1 === num04_2); // false
